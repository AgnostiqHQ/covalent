import sys
from dataclasses import dataclass


@dataclass
class Wait:
    RETRIES: int

    def __int__(self) -> int:
        return self.RETRIES


LONG = Wait(3000)
VERY_LONG = Wait(60000)
EXTREME = Wait(sys.maxsize)
